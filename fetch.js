const newItems = new Set([]);

let iterationCount = 1;
let lastLastItemsSize;
let lastItemsSize;

const loopThroughAll = async () => {
  const keys = Object.keys(items);
  const size = keys.length;

  lastLastItemsSize = lastItemsSize;
  lastItemsSize = size;

  for (let i = 0; i < size; i++) {
    for (let j = i; j < size; j++) {
      const item1 = keys[i];
      const item2 = keys[j];

      const hash = `${item1},${item2}`;

      if (tried.has(hash)) {
        console.log(`Already tried ${item1},${item2}. Skipping...`);
        continue;
      }

      console.log(`Fetching ${item1},${item2}`);

      let data;

      try {
        const response = await fetch(
          `https://neal.fun/api/infinite-craft/pair?first=${item1}&second=${item2}`
        );
        data = await response.json();
      } catch (error) {
        console.log("Failed, probably because of rate limiting.");
        console.log(`Final data object:`, {
          items,
          tried: Array.from(tried),
          newItems: Array.from(newItems),
        });
        throw error;
      }

      if (data?.result) {
        const result = data.result;

        console.log(`Result: ${result}`);

        if (!items[result]) {
          items[result] = [];
        }

        items[result] = [...items[result], [item1, item2]];

        if (data.isNew) {
          newItems.add(data.result);
        }
      } else {
        console.log("No result");
      }

      tried.add(hash);

      if (iterationCount % 100 === 0) {
        console.log(
          `Finished ${iterationCount} iterations. Here's the most up to date data object:`,
          {
            items,
            tried: Array.from(tried),
            newItems: Array.from(newItems),
          }
        );
      }

      iterationCount++;
    }
  }

  console.log("\n\n\n\n");
};

const run = async () => {
  while (
    !lastLastItemsSize ||
    !lastItemsSize ||
    lastLastItemsSize !== lastItemsSize
  ) {
    await loopThroughAll();
  }
};

run();
